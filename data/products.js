export function getProductBySlug(slug) {
  return products.find(p => p.slug === slug) || null
}

export function getProductsByCategory(category) {
  if (!category || category === 'all' || category === 'featured') return products
  return products.filter(p =>
    p.category.toLowerCase().replace(/\s+/g, '-') === category.toLowerCase() ||
    p.subcategory.toLowerCase().replace(/\s+/g, '-') === category.toLowerCase()
  )
}

export function getRelatedProducts(product, count = 3) {
  return products
    .filter(p => p.id !== product.id && p.category === product.category)
    .slice(0, count)
}

export function formatPrice(price) {
  return 'PKR ' + price.toLocaleString('en-PK')
}
